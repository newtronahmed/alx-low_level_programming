#include "3-calc.h"
/**
 * op_add - sum of a and b
 *
 * @a: param
 * @b: param
 *
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return a - b
 *
 * @a: param
 * @b: param
 *
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return a times b
 *
 * @a: param
 * @b: param
 *
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div- return a divided by  b
 *
 * @a: param
 * @b: param
 *
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod- return a modulo  b
 *
 * @a: param
 * @b: param
 *
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
